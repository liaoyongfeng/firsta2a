import { Skill } from './types';

// 导师角色定义
export interface Mentor {
  id: string;
  name: string;
  avatar: string;
  title: string;
  introduction: string;
}

// 对话消息类型
export interface ChatMessage {
  id: string;
  role: 'mentor' | 'student';
  content: string;
  timestamp?: number;
  type?: 'text' | 'plan' | 'achievement' | 'levelup' | 'result';
  metadata?: {
    skills?: Skill[];
    connections?: string[];
    levelUp?: {
      from: number;
      to: number;
    };
  };
}

// 咨询阶段类型
export type ConsultationPhase = 
  | 'greeting'      // 欢迎阶段
  | 'requirement'   // 需求沟通
  | 'proposal'      // 方案建议
  | 'negotiation'   // 协商调整
  | 'confirmation'  // 等待确认
  | 'achievement'   // 学习计划后的收获
  | 'levelup'       // 技能升级氛围展示
  | 'result';       // 完整学习成果

// 咨询场景定义
export interface ConsultationScenario {
  id: string;
  title: string;
  description: string;
  tags: string[];
  messages: ChatMessage[];
  // 第一个确认点：确认学习计划（在协商调整后）
  confirmPlanIndex: number;
  // 第二个确认点：确认开始学习（在展示学习收获后）
  confirmStartIndex: number;
  result: {
    newConnections: string[];
    newSkills: Skill[];
    levelUp: {
      from: number;
      to: number;
    };
  };
}

